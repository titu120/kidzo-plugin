    /**
     * Render rsgallery widget output on the frontend.
     *
     * Written in PHP and used to generate the final HTML.
     *
     * @since 1.0.0
     * @access protected
     */
    protected function render()
    {
        $settings = $this->get_settings_for_display();
        ?>
        <section class="grt-counter-section-3 fix section-padding section-bg">
            <div class="container">
                <div class="grt-counter-wrapper-3">
                    <?php
                    if ($settings['counter_list']) {
                        $i = 0;
                        foreach ($settings['counter_list'] as $item) {
                            $i++;
                            $delay = .2 * $i;
                            ?>
                            <div class="grt-counter-items-3 wow fadeInUp" data-wow-delay="<?php echo esc_attr($delay); ?>s">
                                <h2 class="title">
                                    <span class="count"><?php echo esc_html($item['count_number']); ?></span><span
                                        class="suffix"><?php echo esc_html($item['count_suffix']); ?></span>
                                </h2>
                                <p>
                                    <?php echo wp_kses_post($item['count_title']); ?>
                                </p>
                            </div>
                            <?php
                        }
                    }
                    ?>
                </div>
            </div>
        </section>
        <?php
    }
